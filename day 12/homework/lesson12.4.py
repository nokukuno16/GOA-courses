first_num = int(input("Enter your number: "))
second_num = int(input("Enter your second number: "))
action = input("Enter action: +, - *, / ")

if action == "+":
    print(first_num + second_num)

if action == "-":
    print(first_num - second_num)

if action == "*":
    print(first_num * second_num)

if action == "/":
    print(first_num / second_num)